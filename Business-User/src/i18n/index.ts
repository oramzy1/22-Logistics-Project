const reverseMap: Record<string, string> = {};
Object.entries(resources.en.translation).forEach(([key, value]) => {
  reverseMap[value as string] = key;
});

export const getKeyForValue = (value: string): string =>
  reverseMap[value] ?? value; // returns the key if found, else the original string

const LANG_KEY = "@22log_lang";

export const LANGUAGES = [
  { code: "en", label: "English", nativeLabel: "English" },
  { code: "ha", label: "Hausa", nativeLabel: "Hausa" },
  { code: "ig", label: "Igbo", nativeLabel: "Igbo" },
  { code: "yo", label: "Yoruba", nativeLabel: "Yorùbá" },
  { code: "fr", label: "French", nativeLabel: "Français" },
  { code: "ar", label: "Arabic", nativeLabel: "العربية" },
] as const;

export type LangCode = (typeof LANGUAGES)[number]["code"];

// Configure i18next immediately with a default so the instance is always valid
i18n.use(initReactI18next).init({
  resources,
  lng: "en", // safe default — overridden by initI18n()
  fallbackLng: "en",
  interpolation: { escapeValue: false },
  detection: undefined,
});

// Call this in _layout.tsx to apply the persisted/device language
export const initI18n = async (): Promise<void> => {
  const stored = await AsyncStorage.getItem(LANG_KEY);
  const deviceLang = Localization.getLocales()[0]?.languageCode ?? "en";
  const preferred = stored ?? deviceLang;
  const safeLng = (
    LANGUAGES.find((l) => l.code === preferred) ? preferred : "en"
  ) as LangCode;

  if (i18n.language !== safeLng) {
    await i18n.changeLanguage(safeLng);
  }
};

export const changeLanguage = async (code: LangCode): Promise<void> => {
  await i18n.changeLanguage(code);
  await AsyncStorage.setItem(LANG_KEY, code);
};

